"""one-shot generator for the 30 hand-designed eval cases.

each entry below is a tight (demographics + economic + situation + behavior) tuple.
run once to materialize jsons under eval/eval_cases/{welfare_only,legal_only,integrated}/.
the cases are held-out (no overlap with src/nyaya_mitra/profile/seeds/).

cohort design (per PLAN B.7):
- welfare-only: at least 1 scheme eligible, 0 frameworks applicable
- legal-only:   0 schemes eligible (or only pmsby), 1 framework applicable
- integrated:   >=1 scheme AND >=1 framework
"""

from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent / "eval" / "eval_cases"


def _profile(
    *,
    seed: int,
    gender: str,
    age: int,
    state: str,
    district: str,
    residence: str,
    occupation: str,
    issue: str,
    initial: str,
    language: str = "en",
    literacy: str = "medium",
    trust: str = "neutral",
    verbosity: str = "med",
    monthly_income: int = 0,
    bpl: bool = False,
    holds_land: bool = False,
    has_lpg: bool = False,
    secc: bool = False,
    kuccha: bool = False,
    has_bank: bool = False,
    formally_employed: bool = False,
    is_wage_worker: bool = False,
    is_consumer: bool = False,
    willing_unskilled: bool = True,
    is_professional: bool = False,
    income_tax: bool = False,
    hidden: dict | None = None,
    sensitive: dict | None = None,
) -> dict:
    return {
        "seed": seed,
        "demographics": {
            "gender": gender,
            "age": age,
            "state": state,
            "district": district,
            "residence": residence,
        },
        "economic": {
            "occupation": occupation,
            "holds_cultivable_land": holds_land,
            "monthly_income": monthly_income,
            "bpl_household": bpl,
            "existing_lpg_in_family": has_lpg,
            "is_professional": is_professional,
            "income_tax_payer": income_tax,
            "willing_unskilled_work": willing_unskilled,
            "has_bank_account": has_bank,
            "secc_listed": secc,
            "kuccha_house": kuccha,
            "formally_employed": formally_employed,
            "is_wage_worker": is_wage_worker,
            "is_consumer_disputant": is_consumer,
        },
        "family": {},
        "situation_specific": {
            "presenting_issue": issue,
            "hidden_facts": hidden or {},
            "sensitive_facts": sensitive or {},
        },
        "behavior": {
            "trust_level": trust,
            "verbosity": verbosity,
            "language_preference": language,
            "literacy": literacy,
            "initial_vague_query": initial,
        },
    }


